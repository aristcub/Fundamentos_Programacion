public class Variable{
    
    public static String mostrar(String s){
        return ("Parametro recibido tipo: STRING");
    }
    
    public static String mostrar(int i){
        return ("Parametro recibido tipo: ENTERO");
    }
    
    public static String mostrar(double d){
        return ("Parametro recibido tipo: DOUBLE");
    }
    
    public static String mostrar(char c){
        return ("Parametro recibido tipo: CHAR");
    }
    
    public static String mostrar(boolean b){
        return ("Parametro recibido tipo: BOOLEAN");
    }
    
}